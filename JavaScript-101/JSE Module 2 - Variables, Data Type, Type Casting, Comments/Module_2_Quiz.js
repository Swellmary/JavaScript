let rosePrice = 8;
let lilyPrice = 10;
let tulipsPrice = 2;

let roseQuantity = 70;
let lilyQuantity = 50;
let tulipsQuantity = 120;

let roseValue = rosePrice * roseQuantity;
let lilyValue = lilyPrice * lilyQuantity;
let tulipsValue = tulipsPrice * tulipsQuantity;

let total = roseValue + lilyValue + tulipsValue;

console.log("Rose – Unit price" + rosePrice + ", quantity: " + roseQuantity + ", value: " + roseQuantity);
console.log("Lily – Unit price" + lilyPrice + ", quantity: " + lilyQuantity + ", value: " + lilyQuantity);
console.log("Tulips – Unit price" + tulipsPrice + ", quantity: " + tulipsQuantity + ", value: " + tulipsQuantity);

console.log("Total: " + total);

