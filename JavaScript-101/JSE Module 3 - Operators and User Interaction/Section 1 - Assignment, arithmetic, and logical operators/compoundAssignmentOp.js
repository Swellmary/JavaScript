
// COMPOUND ASSIGNMENT OPERATORS

//Binary arithmetic operators (+, -, /, *, %, **)
//Assignment operator (=)

let x = 10;



x += 2;
//could also be written down as x = x + 2;
console.log(x); // -> 12
x -= 2;
console.log(x); // -> 10
x /= 2;
console.log(x); // -> 512
x *= 2;
console.log(x); // -> 10
x %= 7;
console.log(x); // -> 3
x **= 2;
console.log(x); // -> 9

x+=100; // x = x + 100; more like += is a shortcut thing
console.log(x);
