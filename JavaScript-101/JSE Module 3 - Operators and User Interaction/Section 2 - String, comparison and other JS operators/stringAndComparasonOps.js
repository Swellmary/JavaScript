// LOGICAL OPERATORS


console.log(10 === "10"); //false
console.log(10 == "10"); // true

console.log(10 === 10); // true
console.log(10 == 10); // true

console.log(10 !== "10"); //true, the === turns to == when using !(not)
console.log(10 != "10"); // false, the == turns to = when using !(not)

console.log(10 !== 10); // false
console.log(10 != 10); // false